def deposit(self, n):
    if n > (capacity - size):
        raise ValueError("It exceed the cookie jar's capacity")
    return size + n

def withdraw(self, n):
    if n > size:
        raise ValueError("There aren't thath many cookies in the jar")
    return size - n


@property
def capacity(self):
    if not capacity >= 0:
        raise ValueError("Capacity is not a non-negative int")
    return capacity

